package practice;

public class DataTypeClass {

	public static void main(String[] args) {
		
		byte EmplyId=123;
		Short value=30000;
		String firstName="Karishma";
		String lastName= "Begum";
		
		
		String fullName= firstName + lastName;
		int sum1 = 1500;
		int sum2 = 500;
		int total = sum1 + sum2;
		
		System.out.println("FirstName: " +firstName);
		System.out.println("LastName : " +lastName);
		System.out.println("Full Name: " +fullName);
		System.out.println("Full Name: Salman khan");
		
		System.out.println("Total value of Sum1 and Sum2 is: "+total);
		//primitive DataTypes
		
		
		
		
		int x=12;
		int y=12;
		
		int sum=100000;
		
		
		long MyNum=1000000000L;
		
		float Num=7.86f;
		
		double MNum=20.066d;
		
		
		System.out.println("EmplyId:"+EmplyId);//byte
		System.out.println(x+y);//sum of two numbers
		
		System.out.println(value);//short
		System.out.println(sum);//int
		System.out.println(Num);//float
		System.out.println(MyNum);//long
		System.out.println(MNum);//double
		
				
		
		

	}

}
